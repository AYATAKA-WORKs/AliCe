void speaker_on(int frq, float beat, int bpm) {

	if (frq != REST) {
		TPU5.TGRA = (int) (12500000 / frq) - 1;
		TPU5.TGRB = TPU5.TGRA / 2;
		CSTR(TPU5) = 1; //カウンタ開始
	}
	wait_timer((int) (60000.0f / bpm * 4.0f / beat - 10.0f));
	CSTR(TPU5) = 0; //カウンタ停止
	PORT3.PODR.BIT.B1 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
	PORT2.PODR.BIT.B7 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
	PORT5.PODR.BIT.B5 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
	PORT5.PODR.BIT.B4 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
	PORTA.PODR.BIT.B4 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
	PORTE.PODR.BIT.B5 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
	wait_timer(10);
}

void buzzer_on(int frq, float beat, int bpm) {

	if (frq != REST) {
		TPU5.TGRA = (int) (12500000 / frq) - 1;
		TPU5.TGRB = TPU5.TGRA / 2;
		CSTR(TPU5) = 1; //カウンタ開始
	}
	wait_timer((int) (60000.0f / bpm * 4.0f / beat - 10.0f));
	CSTR(TPU5) = 0; //カウンタ停止
	wait_timer(10);
}

void speaker_on2(int frq, float beat, int seqNum, int *nowSeq, int bpm) {
	if (seqNum == *nowSeq) {
		if (frq != REST) {
			TPU5.TGRA = (int) (12500000 / frq) - 1;
			TPU5.TGRB = TPU5.TGRA / 2;
			CSTR(TPU5) = 1; //カウンタ開始
		}
		if (counter((int) (60000.0f / bpm * 4.0f / beat))) {
			CSTR(TPU5) = 0; //カウンタ停止
			PORT3.PODR.BIT.B1 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
			PORT2.PODR.BIT.B7 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
			PORT5.PODR.BIT.B5 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
			PORT5.PODR.BIT.B4 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
			PORTA.PODR.BIT.B4 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
			PORTE.PODR.BIT.B5 = (int) (rand() * (2.0) / (1.0 + RAND_MAX));
			(*nowSeq)++;
		}
	}
}

void SpeakerDisable(void) {
	PORTB.PMR.BIT.B7 = 0x0u;	//PB7:I/Oを使用
	PORTB.PDR.BIT.B7 = 1;		//PB7：出力
	PORTB.PODR.BIT.B7 = 0;		//PB7：L
}

void SpeakerEnable(void) {
	PORTB.PMR.BIT.B7 = 0x1u;	//PB7:周辺機能を使用
}

void StartUpSound(int bpm, int pitch) {
	speaker_on(pitch * A_4, 8.000000, bpm);
	speaker_on(pitch * REST, 8.000000, bpm);
	speaker_on(pitch * F_4, 2.666667, bpm);
	speaker_on(pitch * REST, 32.000000, bpm);
	speaker_on(pitch * C_4, 8.000000, bpm);
	speaker_on(pitch * REST, 8.000000, bpm);
	speaker_on(pitch * C_5, 8.000000, bpm);
	speaker_on(pitch * REST, 4.000000, bpm);
	speaker_on(pitch * A_4, 1.432836, bpm);
}

